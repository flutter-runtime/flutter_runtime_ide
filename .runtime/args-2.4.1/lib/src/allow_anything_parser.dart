class $AllowAnythingParser$ {

dynamic get(String name) {
  if (name == "options") return '';
  if (name == "commands") return '';
  if (name == "allowTrailingOptions") return '';
  if (name == "allowsAnything") return '';
  if (name == "usageLineLength") return '';
  if (name == "usage") return '';
}

}
